// assigning my age
const myAge = 22;

// early years value
let earlyYears = 2;

earlyYears *= 10.5;

let laterYears = myAge - 2;

laterYears *= 4;

// adding to get age in dog years
let myAgeInDogYears = earlyYears + laterYears;

let myName = 'Oliver'.toLowerCase();

console.log(`My name is ${myName}. I am ${myAgeInDogYears} years old in dog years`);
